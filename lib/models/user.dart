class User {
  final String Id;
  final String username;
  final String nama;
  final String loc;
  final String password;
  final String profil;
  final String desc;

  User({
    required this.Id,
    required this.username,
    required this.password,
    required this.profil,
    required this.desc,
    required this.loc,
    required this.nama,
  });
}
